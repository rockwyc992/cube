package cube;

class Point {
    
    float x, y, z;
    
    Point() {
    }
    
    Point(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    void set_x(float x) {
        this.x = x;
    }

    void set_y(float y) {
        this.y = y;
    }

    void set_z(float z) {
        this.z = z;
    }
}

